package com.github.mmakart.testTaskDeeplay.util;

public class InputStringFormatException extends IllegalArgumentException {

	private static final long serialVersionUID = 6320931888738251343L;

	public InputStringFormatException() {
		super();
	}

	public InputStringFormatException(String message) {
		super(message);
	}

	public InputStringFormatException(Throwable cause) {
		super(cause);
	}

	public InputStringFormatException(String message, Throwable cause) {
		super(message, cause);
	}

}
